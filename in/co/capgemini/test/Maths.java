package in.co.capgemini.test;

//class Maths to perform operations 
public class Maths {
	public int add(int a, int b) {
		return a + b;
	}

	public int sub(int i, int j) {
		return i - j;
	}

	public int mul(int i, int j) {
		return i * j;
	}

	public int div(int i, int j) {
		return i / j;
	}
}
